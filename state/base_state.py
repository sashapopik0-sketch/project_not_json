"""Модуль базового класса состояния для паттерна 'Состояние'."""

from abc import ABC, abstractmethod
from typing import List
from core.note import Note


class BaseState(ABC):
    """Абстрактный базовый класс для реализации паттерна 'Состояние'.

    Определяет общий интерфейс для всех конкретных состояний управления
    данными заметок. Каждое конкретное состояние должно реализовать методы
    для загрузки и сохранения заметок.

    Attributes:
        notes: Список объектов Note для управления данными.
    """

    @abstractmethod
    def load_notes(self) -> List[Note]:
        """Загружает список заметок из источника данных.

        Абстрактный метод, который должен быть реализован в дочерних классах.
        Отвечает за чтение данных заметок из соответствующего хранилища
        (например, JSON-файл, база данных, память).

        Returns:
            Список объектов Note, загруженных из источника данных.

        Raises:
            NotImplementedError: Если метод не реализован в дочернем классе.
        """
        pass

    @abstractmethod
    def save_notes(self, notes: List[Note]) -> None:
        """Сохраняет список заметок в источник данных.

        Абстрактный метод, который должен быть реализован в дочерних классах.
        Отвечает за запись данных заметок в соответствующее хранилище.

        Args:
            notes: Список объектов Note для сохранения.

        Raises:
            NotImplementedError: Если метод не реализован в дочернем классе.
        """
        pass